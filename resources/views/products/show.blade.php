    <script>
        function decrementQuantity() {
            var inputElement = document.getElementById('second_product');
            var currentValue = parseInt(inputElement.value);
            if (currentValue > 0) {
                inputElement.value = currentValue - 1;
            }
        }

        function incrementQuantity() {
            var inputElement = document.getElementById('second_product');
            var currentValue = parseInt(inputElement.value);
            inputElement.value = currentValue + 1;
        }
